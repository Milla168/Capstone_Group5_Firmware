// Setup
// ---------------------------------------------------------------------------

void setup() {
  Serial.begin(115200);
  while (!Serial) delay(10);

//   Serial.println("==============================");
//   Serial.println("Crochet Stitch Detector");
//   Serial.println("==============================");

  // --- IMU ---
//   Serial.print("[LOG] Initialising LSM6DSOX... ");
  if (!imu.begin_I2C()) {
    // Serial.println("FAILED");
    while (1) delay(10);
  }
  imu.setAccelRange(LSM6DS_ACCEL_RANGE_2_G);
  imu.setGyroRange(LSM6DS_GYRO_RANGE_250_DPS);
  imu.setAccelDataRate(LSM6DS_RATE_104_HZ);
  imu.setGyroDataRate(LSM6DS_RATE_104_HZ);
  Serial.println("OK");

  // --- TFLite model ---
//   Serial.print("[LOG] Loading TFLite model... ");
  tfl_model = tflite::GetModel(model_data);
  if (tfl_model->version() != TFLITE_SCHEMA_VERSION) {
    // Serial.println("FAILED - schema version mismatch");
    while (1) delay(10);
  }
//   Serial.println("OK");

  static tflite::AllOpsResolver resolver;
  static tflite::MicroInterpreter static_interpreter(
    tfl_model, resolver, tensor_arena, TENSOR_ARENA_SIZE, error_reporter
  );
  tfl_interpreter = &static_interpreter;

//   Serial.print("[LOG] Allocating tensors... ");
  if (tfl_interpreter->AllocateTensors() != kTfLiteOk) {
    // Serial.println("FAILED - try increasing TENSOR_ARENA_SIZE");
    while (1) delay(10);
  }
//   Serial.println("OK");

  tfl_input  = tfl_interpreter->input(0);
  tfl_output = tfl_interpreter->output(0);

  // Sanity checks -- these will tell you immediately if the wrong model was flashed
//   Serial.print("[LOG] Input  dtype: ");
//   Serial.println(tfl_input->type  == kTfLiteInt8 ? "int8 (correct)" : "WRONG - expected int8");
//   Serial.print("[LOG] Output dtype: ");
//   Serial.println(tfl_output->type == kTfLiteInt8 ? "int8 (correct)" : "WRONG - expected int8");

//   Serial.print("[LOG] Input shape:  [");
//   for (int i = 0; i < tfl_input->dims->size; i++) {
//     Serial.print(tfl_input->dims->data[i]);
//     if (i < tfl_input->dims->size - 1) Serial.print(", ");
//   }
//   Serial.println("]  (expected [1, 200, 6])");

//   Serial.print("[LOG] Arena used:   ");
//   Serial.print(tfl_interpreter->arena_used_bytes() / 1024);
//   Serial.println(" KB");

//   Serial.println("[LOG] Ready - collecting samples...");
//   Serial.println("==============================");

  memset(sample_buf, 0, sizeof(sample_buf));
  last_sample_us = micros();
}

// ---------------------------------------------------------------------------
// Loop
// ---------------------------------------------------------------------------

void loop() {
  unsigned long now = micros();
  if (now - last_sample_us < SAMPLE_INTERVAL_US) return;
  last_sample_us += SAMPLE_INTERVAL_US;

  // --- Read IMU ---
  sensors_event_t accel_evt, gyro_evt, temp_evt;
  imu.getEvent(&accel_evt, &gyro_evt, &temp_evt);

  // Match training units: m/s2 -> g,  rad/s -> deg/s
  float ax = accel_evt.acceleration.x / 9.81f;
  float ay = accel_evt.acceleration.y / 9.81f;
  float az = accel_evt.acceleration.z / 9.81f;
  float gx = gyro_evt.gyro.x * 57.2958f;
  float gy = gyro_evt.gyro.y * 57.2958f;
  float gz = gyro_evt.gyro.z * 57.2958f;

  // Cache for the debug print in run_inference()
  last_ax = ax; last_ay = ay; last_az = az;
  last_gx = gx; last_gy = gy; last_gz = gz;

  // --- Store in circular buffer ---
  sample_buf[buf_head][0] = ax;
  sample_buf[buf_head][1] = ay;
  sample_buf[buf_head][2] = az;
  sample_buf[buf_head][3] = gx;
  sample_buf[buf_head][4] = gy;
  sample_buf[buf_head][5] = gz;

  buf_head = (buf_head + 1) % WINDOW_LENGTH;
  if (samples_in_buf < WINDOW_LENGTH) samples_in_buf++;
  samples_since_last_inference++;

  // --- Inference every STRIDE samples once the buffer is full ---
  if (samples_in_buf == WINDOW_LENGTH &&
      samples_since_last_inference >= STRIDE) {
    samples_since_last_inference = 0;
    run_inference();
  }
}





