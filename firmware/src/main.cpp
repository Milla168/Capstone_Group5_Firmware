// src/main.cpp
//
// Crochet Stitch Detector -- ESP32 + LSM6DSOX + TFLite (INT8)
//
// Pipeline (mirrors Python training exactly):
//   1. Sample IMU at 100 Hz  ->  ax_g, ay_g, az_g, gx_dps, gy_dps, gz_dps
//   2. Accumulate 200 samples into a circular buffer (WINDOW_LENGTH)
//   3. Every 100 new samples (STRIDE):
//        a. Normalise: norm = (raw - mean) / std
//        b. Quantize:  int8 = clamp(round(norm / input_scale) + input_zp, -128, 127)
//        c. Run inference
//        d. Dequantize output: prob = (int8_out - output_zp) * output_scale
//   4. Print IMU readings + inference result to Serial
//
// If IMU reads all zeros, that means the sensor lost I2C contact.
// The [IMU ZERO] warning below will fire so you know to power-cycle
// without having to interpret raw numbers.

