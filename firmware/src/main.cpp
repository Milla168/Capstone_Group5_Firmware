// ---------------------------------------------------------------------------
// Configuration -- must match Python config.py
// ---------------------------------------------------------------------------

static const int   WINDOW_LENGTH  = 200;
static const int   STRIDE         = 100;
static const int   NUM_CHANNELS   = 6;
static const int   SAMPLE_RATE_HZ = 100;
static const float THRESHOLD      = 0.5f;

// ---------------------------------------------------------------------------
// Normalization constants -- from models/scaler/mean.npy and std.npy
// Order: [ax_g, ay_g, az_g, gx_dps, gy_dps, gz_dps]
// ---------------------------------------------------------------------------

static const float NORM_MEAN[NUM_CHANNELS] = {
  -0.16761149f,   // ax_g
  -0.67390168f,   // ay_g
   0.10533028f,   // az_g
  -0.25865123f,   // gx_dps
   1.07713866f,   // gy_dps
   0.82166719f    // gz_dps
};

static const float NORM_STD[NUM_CHANNELS] = {
   0.27730149f,   // ax_g
   0.34247386f,   // ay_g
   0.59229612f,   // az_g
  70.27664948f,   // gx_dps
  40.82910156f,   // gy_dps
  41.08145142f    // gz_dps
};

// ---------------------------------------------------------------------------
// INT8 quantization parameters -- from esp32_deployment_notes.txt
// Update these if you reconvert the model.
// ---------------------------------------------------------------------------

static const float INPUT_SCALE       =  0.03121445f;
static const int   INPUT_ZERO_POINT  = -4;
static const float OUTPUT_SCALE      =  0.00390625f;
static const int   OUTPUT_ZERO_POINT = -128;

// ---------------------------------------------------------------------------
// TFLite
// ---------------------------------------------------------------------------

static const int TENSOR_ARENA_SIZE = 60 * 1024;
static uint8_t   tensor_arena[TENSOR_ARENA_SIZE];

static tflite::MicroErrorReporter micro_error_reporter;
static tflite::ErrorReporter*     error_reporter  = &micro_error_reporter;
static const tflite::Model*       tfl_model       = nullptr;
static tflite::MicroInterpreter*  tfl_interpreter = nullptr;
static TfLiteTensor*              tfl_input       = nullptr;
static TfLiteTensor*              tfl_output      = nullptr;

// ---------------------------------------------------------------------------
// Sampling state
// ---------------------------------------------------------------------------

static float         sample_buf[WINDOW_LENGTH][NUM_CHANNELS];
static int           buf_head                     = 0;
static int           samples_in_buf               = 0;
static int           samples_since_last_inference  = 0;

// Most recent raw IMU values -- updated every sample, printed at inference time
static float last_ax = 0, last_ay = 0, last_az = 0;
static float last_gx = 0, last_gy = 0, last_gz = 0;

// ---------------------------------------------------------------------------
// Stitch counting + debounce
//
// Problem: one physical stitch spans ~300 samples but stride=100, so it
// shows up in 2-3 consecutive windows all above threshold.
//
// Solution: rising-edge detection + cooldown.
//   - Only count when transitioning from non-stitch -> stitch (rising edge).
//   - After counting, suppress for DEBOUNCE_WINDOWS windows so the tail
//     of the same motion can't re-trigger.
//
// DEBOUNCE_WINDOWS=2 means ~2s suppression after each count.
// Lower = more responsive but risks double-counts on slow stitches.
// ---------------------------------------------------------------------------

static const int DEBOUNCE_WINDOWS  = 1;
static int       stitch_count      = 0;
static int       debounce_remaining = 0;
static int       prev_prediction   = 0;

Adafruit_LSM6DSOX imu;

static unsigned long       last_sample_us     = 0;
static const unsigned long SAMPLE_INTERVAL_US = 1000000UL / SAMPLE_RATE_HZ;

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

static inline int8_t clamp_to_int8(float v) {
  if (v < -128.0f) return -128;
  if (v >  127.0f) return  127;
  return (int8_t) roundf(v);
}

// Normalise then quantize the circular buffer into the INT8 input tensor
void fill_quantize_input() {
  int8_t* input_data = tfl_input->data.int8;

  for (int i = 0; i < WINDOW_LENGTH; i++) {
    int src = (buf_head + i) % WINDOW_LENGTH;
    for (int ch = 0; ch < NUM_CHANNELS; ch++) {
      float norm      = (sample_buf[src][ch] - NORM_MEAN[ch]) / NORM_STD[ch];
      float quantized =  norm / INPUT_SCALE + INPUT_ZERO_POINT;
      input_data[i * NUM_CHANNELS + ch] = clamp_to_int8(quantized);
    }
  }
}

void run_inference() {
  fill_quantize_input();

  TfLiteStatus status = tfl_interpreter->Invoke();
  if (status != kTfLiteOk) {
    // Serial.println("[ERROR] Inference failed");
    return;
  }

  // Dequantize INT8 output -> probability [0, 1]
  int8_t raw_out    = tfl_output->data.int8[0];
  float  prob       = (raw_out - OUTPUT_ZERO_POINT) * OUTPUT_SCALE;
  int    prediction = (prob >= THRESHOLD) ? 1 : 0;

  // --- Rising-edge detection + debounce ---
  if (debounce_remaining > 0) debounce_remaining--;

  if (prediction == 1 && prev_prediction == 0 && debounce_remaining == 0) {
    stitch_count++;
    debounce_remaining = DEBOUNCE_WINDOWS;
    Serial.print(">>> STITCH DETECTED total=");
    Serial.println(stitch_count);
  }
  prev_prediction = prediction;

  // IMU zero warning
  bool imu_zero = (last_ax == 0.0f && last_ay == 0.0f && last_az == 0.0f &&
                   last_gx == 0.0f && last_gy == 0.0f && last_gz == 0.0f);
  if (imu_zero) Serial.println("<<< IMU ZERO - check wiring / power-cycle >>>");

  // Debug prints (uncomment to diagnose)
//  Serial.print("[IMU] ax="); Serial.print(last_ax, 3);
//  Serial.print(" ay=");      Serial.print(last_ay, 3);
//  Serial.print(" az=");      Serial.print(last_az, 3);
//  Serial.print(" | gx=");    Serial.print(last_gx, 1);
//  Serial.print(" gy=");      Serial.print(last_gy, 1);
//  Serial.print(" gz=");      Serial.print(last_gz, 1);
//  Serial.println();
//  Serial.print("[INF] prob=");  Serial.print(prob, 4);
//  Serial.print("  raw=");       Serial.print(raw_out);
//  Serial.print("  cooldown=");  Serial.print(debounce_remaining);
//  Serial.print("  stitches=");  Serial.println(stitch_count);
//  Serial.println();
}

